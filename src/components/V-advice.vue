Температурный режим в уходе за цветами
Растения не зря получили название «комнатные» — им действительно комфортно при такой температуре. Большинство цветов развиваются лучше всего при 13-24 градусах, и только экзотические сорта требуют более высоких показателей. Некоторые наоборот лучше растут в холоде. Для них максимальная отметка тепла — 16 градусов: при более высоких режимах листья начинают портиться.

Теплолюбивые растения расцветают при температуре от 16 градусов тепла. Большинство цветов способны выдержать небольшие отклонения от нормы без видимого ущерба. Они смогут пережить некоторый период в холоде или жаре. Главное — избегать резких перепадов температур. Только кактусы и суккуленты могут справиться с неожиданной сменой режима: в природе они обитают в местах, где днем жарко, а ночью прохладно.


Освещение для растений
Чтобы цветок мог расти и развиваться, важно обеспечить для него подходящий режим освещения. Если это условие не выполняется, то процессы синтеза замедляются, цветок не получает нужных питательных веществ и начинает погибать. Самый простой способ обеспечить доступ к свету — поставить растение у окна. Можно использовать и искусственное освещение (не менее 12 часов в день). 

Не забывайте про такой параметр, как интенсивность освещения. Одни растения требуют яркого солнечного света и тянутся к нему, а другие лучше растут в тени. Регулировать яркость освещения можно и в пределах одной комнаты. Достаточно отойти на несколько шагов от окна и интенсивность света уменьшится в половину. Если уйти вглубь комнаты, свет сократится на 95%. Выбрав подходящее для цветка место, вы увидите, как он активно растет и цветет.

