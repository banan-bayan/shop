Полив комнатных растений
Частота полива зависит не только от типа цветка, но также от температуры воздуха, влагоемкости почвы, освещения и многих других особенностей. Алоэ, агава и другие растения с сочными листьями требуют меньше влаги, чем гибискусы и другие виды с крупными листьями. Луковичным растениям противопоказан чрезмерный полив — луковица может просто сгнить.

Обильного полива требуют растения цветущие и находящиеся в фазе роста, с большим количеством листьев, с мощной корневой системой. Дополнительная влага понадобится цветкам в глиняных горшках с крупными дренажными отверстиями. Сигналом к частому поливу считается и жаркая погода, и сухой теплый воздух зимой. При любых сомнениях помните: лучше поливать понемногу и часто, чем редко и обильно.


Для полива больше всего подходит мягкая дождевая вода. Водопроводную лучше предварительно пропустить через фильтр, чтобы исключить из нее соли, которые противопоказаны азалиям, орхидеям, папоротникам, камелиям. Чтобы смягчить воду, в нее можно добавить древесную золу (3 г на литр) или торф (10 г на литр). Подойдет также кипяченая вода.

Обобщенный режим полива в разные времена года:

лето — каждый день;
весна — через день;
осень — каждые 2-3 дня;
зима — раз в 2 недели.
Внимательно присмотритесь к цветам. Заметили, что листья поникли, бутоны завяли, а земля сухая? Срочно спасайте питомца: все говорит о недостатке воды. Об избытке влаги можно судить по коричневым кончикам листьев, налете на стеблях, плесени на поверхности земли и замедлению роста. В этом случае уберите влагу с помощью бумажных салфеток и проверьте состояние корневой системы. Если она повреждена, то попробуйте удалить потемневшие и подгнившие корни при помощи обычных ножниц, а затем посадите цветок в новую почву. 

Как и чем подкармливать растения?
Если растения страдают от недостатка питания, им требуется дополнительная подкормка: почва истощается и не может дать нужных веществ. В таком случае прежде всего нужно пересадить растение в новый грунт, а через 2 месяца начать подкармливать его. Удобрения понадобятся и в том случае, если вы посадили растение в неподходящий грунт. Понять, какого именно компонента не хватает для нормального роста тяжело, поэтому лучше всего пересадить растение.


Удобрять рекомендуется только здоровые растения в период их цветения и роста. В регулярной подкормке особенно нуждаются быстрорастущие цветы. Вносить удобрения в сухом виде не стоит — так можно повредить корни. Лучше развести вещества с водой и поливать землю. Удобрения делятся на:

азотные;
фосфорно-кислые;
калийные.
Азотные усиливают рост стеблей и листьев, но их избыток грозит грибковыми заболеваниями и задержкой цветения. Фосфорно-кислые способствуют появлению большего количества почек и длительному цветению. Калийные усиливают рост и цветение, поэтому используются в период появления бутонов. Избыток удобрений всегда негативно сказывается на растениях, поэтому их количество важно контролировать и подбирать оптимальный объем.

Влажность воздуха в уходе за растениями
Недостаточная или чрезмерная влажность воздуха тоже отражается на состоянии комнатной флоры. Летом и весной влажность воздуха в квартире не опускается ниже 50%, а зимой воздух становится сухим из-за отопления. Самый простой способ поддерживать нужный уровень влаги в воздухе — это опрыскивание. Большинство растений можно опрыскивать ежедневно. Нельзя опрыскивать фиалки, глоксинии и другие цветы с бархатистыми листьями.

Оптимальные условия для опрыскивания — температура 24-26 градусов. В жару листья опрыскивать нельзя: это приведет к ожогам. Влажность воздуха можно повысить с помощью больших емкостей воды или полотенца: поместите его на батарею и регулярно смачивайте. Также поможет специальная техника — увлажнители, которые сами регулируют количество влаги.

